    private void btnDeleteMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMemberMouseClicked

        int selectedRow = tblMember.getSelectedRow();
        int remove_id = (int) tblMember.getModel().getValueAt(selectedRow,0);
        if(selectedRow == -1) JOptionPane.showMessageDialog(btnAdd_AddMember, "Please select row to delete");
        // Nếu có hàng được chọn, hiển thị hộp thoại xác nhận trước khi xóa hàng
        if (selectedRow != -1) {
            int confirmed = JOptionPane.showConfirmDialog(null, 
                "Are you sure you want to delete this row?", 
                "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (confirmed == JOptionPane.YES_OPTION) {
                model.removeRow(selectedRow);
                controller.removeMemberById(remove_id);
            }
        }   
    }//GEN-LAST:event_btnDeleteMemberMouseClicked

    private void btnEditMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMemberMouseClicked
        selectedIndex = tblMember.getSelectedRow();
        int edit_id = 0;
        if (controller.isEmpty()){
            JOptionPane.showMessageDialog(btnAdd_AddMember, "There is nothing to edit");
        }
        else if (selectedIndex == -1){
            JOptionPane.showMessageDialog(btnAdd_AddMember, "Select row to edit");
        }
        else {
            edit_id = (int) tblMember.getModel().getValueAt(selectedIndex,0);
            Member t = controller.getMemberById(edit_id);
            txfName_EditMember.setText(t.getName());    
            txfPersonalId_EditMember.setText(t.getPersonal_id());  
            try {
                dcDateOfBirth_EditMember.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(t.getDate_of_birth()));
            }
            catch (Exception e) {
                System.out.println(e);
            }
            cbGender_EditMember.setSelectedIndex((t.getGender()== Member.Gender.MALE) ? 0 : 1);   
            txfMajor_EditMember.setText(t.getMajor()); 
            if (t.getPosition()== Member.Position.MEMBER) cbPosition_EditMember.setSelectedIndex(0);
            else if (t.getPosition()== Member.Position.LEADER) cbPosition_EditMember.setSelectedIndex(1);
            else cbPosition_EditMember.setSelectedIndex(2);
            txfEmail_EditMember.setText(t.getEmail()); 
            ArrayList<Team> list = controller.getAllTeams();
                for (int i = 0; i < list.size(); ++i) {
                    cbTeam_EditMember.addItem(Integer.toString(list.get(i).getId()));
                }
            jfEditMember.setVisible(true);
        }
    }//GEN-LAST:event_btnEditMemberMouseClicked
    
